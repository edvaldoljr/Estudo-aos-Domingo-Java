public class OperadoresComparacao {
    public static void main(String[] args) {
        int numeroUm = 10;
        int numeroDois = 30;

        System.out.println(numeroUm > numeroDois);
        System.out.println(numeroUm >= numeroDois);
        System.out.println(numeroUm < numeroDois);
        System.out.println(numeroUm <= numeroDois);
        System.out.println(numeroUm == numeroDois);
        System.out.println(numeroUm != numeroDois);
    }
}
