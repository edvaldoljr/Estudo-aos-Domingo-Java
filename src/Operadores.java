public class Operadores {

    public static void main(String[] args) {
        int numeroUm = 20;
        int numeroDois = 30;

        System.out.println(numeroUm + numeroDois);
        System.out.println(numeroUm - numeroDois);
        System.out.println(numeroUm * numeroDois);
        System.out.println(numeroUm / numeroDois);
        System.out.println(numeroUm % numeroDois);
    }
}
